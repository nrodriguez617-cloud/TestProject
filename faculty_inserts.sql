DROP TABLE IF EXISTS Faculty;

CREATE TABLE Faculty (
    FacSSN VARCHAR(11) PRIMARY KEY,
    FacFirstName VARCHAR(50),
    FacLastName VARCHAR(50),
    FacCity VARCHAR(50),
    FacState VARCHAR(2),
    FacDept VARCHAR(10),
    FacRank VARCHAR(10),
    FacSalary NUMERIC(10, 2),
    FacSupervisor VARCHAR(11),
    FacHireDate DATE,
    FacZipCode VARCHAR(10)
);

