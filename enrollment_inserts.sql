CREATE TABLE Enrollment (
    RegNo INT NOT NULL,
    OfferNo INT NOT NULL,
    EnrGrade NUMERIC(3, 2),
    PRIMARY KEY (RegNo, OfferNo),
    FOREIGN KEY (RegNo) REFERENCES Registration(RegNo),
    FOREIGN KEY (OfferNo) REFERENCES Offering(OfferNo)
);


